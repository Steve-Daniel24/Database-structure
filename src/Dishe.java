public class Dishe {
    private String type;
    private String color;
    private String age;

    public Dishe(String type, String color, String age) {
        this.type = type;
        this.color = color;
        this.age = age;
    }

    public String getType() {
        return type;
    }

    public String getColor() {
        return color;
    }

    public String getAge() {
        return age;
    }
}